package com.DataTypes;

public class DataTypesExample {

	public static void main(String[] args) {
		
		System.out.println(" ***** variables & Data Types");
		byte byteVariable=10;
		byteVariable=20;
		System.out.println(" The value of the Variable byte Variable is:- "+byteVariable);
		
		short shortVariable=30;
		System.out.println(" The value of the Variable byte Variable is:- "+shortVariable);
		
		long longVariable=40;
		System.out.println(" The value of the Variable byte Variable is:- "+longVariable);
		
		int intVariable=50;
		System.out.println(" The value of the Variable byte Variable is:- "+intVariable);
		
		double doubleVariable=68.123456789123456789;
		System.out.println(" The value of the Variable byte Variable is:- "+doubleVariable);
		
		float floatVariable=17.1234512345f;
		System.out.println(" The value of the Variable byte Variable is:- "+floatVariable);
		
		char charVariable='a';
        System.out.println(" The value of the Variable byte Variable is:- "+charVariable);
        
        String stringVariable="abc";
        System.out.println(" The value of the Variable byte Variable is:- "+stringVariable);
        
        boolean booleanVariable=true;
        booleanVariable=false;
        System.out.println(" The value of the Variable byte Variable is:- "+booleanVariable);

	}

}
