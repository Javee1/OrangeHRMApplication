package com.Loops;

public class DoWhileExample {

	public static void main(String[] args) {
		
		int var=5;
		
		do
		{
			System.out.println(var+" selenium ");
			var++;
		}while(var<6);
		
		do
		{
			System.out.println(var+" Testing ");
			var++;
		}while(var<=8);
		do
		{
			System.out.println(var+" LiveTech ");
			var=var-2;
		}while(var<=6);

	}

}
