package com.Loops;

public class ForLoopExample {

	public static void main(String[] args) {
		
		for(int a=1;a<=3;a++)
		{
			System.out.println(a+" Testing ");
		}
		
		
		System.out.println(" ******** ");
		
		for(int i=2;i<=8;i=i+2)
		{
			System.out.println(i+" selenium ");
		}
		
		System.out.println(" &&&&& ");
		
		for(int k=-5;k<=0;k=k+3)
		{
			System.out.println(k+" LiveTech ");
		}
		
		System.out.println(" -------- ");
		

		
	}

}
